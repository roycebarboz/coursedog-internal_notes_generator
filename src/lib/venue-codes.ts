/** UCC TechFlex space identifiers for merging logic. */
const UCC_TECHFLEX_LOCATIONS = new Set([
  "UCC Tech Flex Space A (Back)",
  "UCC Tech Flex Space B (Middle)",
  "UCC Tech Flex Space C (Front)",
]);

/** UCC spaces that count toward "more than two different UCC spaces" → use "UCC" code. */
const UCC_DISTINCT_SPACES = new Set([
  "UCC 106 (The Gallery)",
  "UCC The Commons Area",
  "UCC 220 (Student Commons)",
  ...UCC_TECHFLEX_LOCATIONS,
]);

/**
 * Extract the building name from a location string.
 * e.g. "UCC 106 (The Gallery)" → "UCC"
 *      "Babbio 210" → "Babbio"
 *      "Gateway South 122" → "Gateway South"
 */
export function extractBuilding(location: string): string {
  if (!location || location === "-") return "Unknown";
  const loc = location.trim();

  // Handle compound building names first
  const compoundPrefixes = [
    "Gateway South", "Gateway North", "Edwin A. Stevens",
    "Martha Bayard Stevens", "North Building", "ABS Engineering Center",
    "Athletics Facilities", "Castle Point", "Griffith Building",
    "Morton-Peirce-Kidde",
  ];
  for (const prefix of compoundPrefixes) {
    if (loc.startsWith(prefix)) return prefix;
  }

  // Single-word building name (everything before the first space+digit or space+special)
  const match = loc.match(/^([A-Za-z]+)/);
  return match ? match[1] : loc;
}

/**
 * Determine the reservation number venue code for a group of locations
 * within the same building, applying UCC merging rules.
 */
export function getReservationVenueCode(locations: string[]): string {
  if (locations.length === 0) return "UNKNOWN";

  // Check if we need UCC TechFlex merging
  const techFlexLocations = locations.filter((l) => UCC_TECHFLEX_LOCATIONS.has(l));
  const otherUccDistinct = locations.filter(
    (l) => UCC_DISTINCT_SPACES.has(l) && !UCC_TECHFLEX_LOCATIONS.has(l)
  );

  // Rule: if the same event has booked more than two different UCC spaces
  // (Gallery, Commons, TechFlex) then use "UCC"
  if (techFlexLocations.length > 0 && otherUccDistinct.length > 0) {
    return "UCC";
  }

  // TechFlex merging: A+B+C → UCCABC, A+B → UCCAB, etc.
  if (techFlexLocations.length > 1) {
    const hasA = techFlexLocations.some((l) => l.includes("Space A"));
    const hasB = techFlexLocations.some((l) => l.includes("Space B"));
    const hasC = techFlexLocations.some((l) => l.includes("Space C"));

    if (hasA && hasB && hasC) return "UCCABC";
    if (hasA && hasB) return "UCCAB";
    if (hasB && hasC) return "UCCBC";
    if (hasA && hasC) return "UCCAC";
  }

  // For non-merged cases, use the first location's code
  // Filter out Pre-function Space from the primary code if there are other locations
  const primaryLocations = locations.filter((l) => l !== "UCC Pre-function Space");
  const primaryLoc = primaryLocations.length > 0 ? primaryLocations[0] : locations[0];

  return VENUE_CODE_FOR_RESERVATION[primaryLoc] || primaryLoc.replace(/\s+/g, "").toUpperCase();
}

/**
 * Get the display venue label for a group of locations, applying overrides.
 */
export function getVenueLabel(locations: string[]): string {
  // Deduplicate override labels
  const labels = new Set<string>();
  for (const loc of locations) {
    labels.add(VENUE_LABEL_OVERRIDES[loc] || loc);
  }
  // If all resolve to same label (e.g. all TechFlex), return it once
  const unique = [...labels];
  return unique.length === 1 ? unique[0] : unique[0];
}
